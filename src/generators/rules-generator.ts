import { writeFile, mkdir } from 'node:fs/promises';
import { join, dirname } from 'node:path';
import type { StackInfo } from '../analyzers/stack-detector.js';

async function ensureDir(filePath: string) {
  await mkdir(dirname(filePath), { recursive: true });
}

function getBaseRules(stack: StackInfo): string[] {
  const lines: string[] = [
    '## General Guidelines',
    '',
    '- **Token Optimization Priority**: Read only the necessary context. Do not read entire directories or large lockfiles unless explicitly requested.',
    '- **Use AI-Optimized Commands**: When exploring the terminal, PREFER `npx contextslim ls [dir]` instead of `ls`. When reading files, PREFER `npx contextslim cat <file>` instead of `cat`. When making text searches, PREFER `npx contextslim grep <query>`. To understand the skeleton of a file without reading logic, STRICTLY use `npx contextslim map <file>`.',
    '- **Be concise**: Give direct answers. Do not repeat code that already exists in the project unless modifying it.',
    '- **Be modular**: Write small, focused functions with clear responsibilities.',
    '- **Follow existing patterns**: Match the coding style, naming conventions, and patterns already used in this codebase.',
    '- **Prefer edits over rewrites**: When modifying code, show only the changed parts or use strict replacements.',
    '',
  ];

  if (stack.language === 'TypeScript' || stack.language === 'JavaScript') {
    lines.push(
      `## Stack: ${stack.name}`,
      '',
      '- Use **ES Modules** (`import`/`export`) — not CommonJS (`require`).',
      '- Prefer `const` over `let`. Never use `var`.',
      '- Use **async/await** over raw Promises or callbacks.',
      '- Use optional chaining (`?.`) and nullish coalescing (`??`).',
      '- Handle errors gracefully with try/catch.',
    );

    if (stack.frameworks && stack.frameworks.length > 0) {
      if (stack.frameworks.includes('React') || stack.frameworks.includes('Next.js')) {
        lines.push(
          '- Write functional React components using hooks.',
          '- Keep effect dependencies accurate.',
        );
      }
      if (stack.frameworks.includes('Next.js')) {
        lines.push(
          '- Follow Next.js App Router conventions (`page.tsx`, `layout.tsx`).',
        );
      }
      if (stack.frameworks.includes('Vue') || stack.frameworks.includes('Nuxt')) {
        lines.push('- Use Vue 3 Composition API with `<script setup>`.');
      }
    }

    if (stack.hasTypeScript) {
      lines.push(
        '- Define explicit **TypeScript types** for function parameters and return values.',
        '- Use `interface` for object shapes, `type` for unions/intersections.',
        '- Avoid `any` — use `unknown` when the type is truly uncertain.',
      );
    }
  } else if (stack.language === 'Python') {
    lines.push(
      `## Stack: ${stack.name}`,
      '',
      '- Use **type hints** for all function signatures.',
      '- Follow **PEP 8** style conventions.',
      '- Prefer **pathlib** over os.path for file operations.',
    );
  } else if (stack.language === 'Rust') {
    lines.push(
      `## Stack: ${stack.name}`,
      '',
      '- Follow **Rust idioms**: use `Result<T, E>` for error handling.',
      '- Minimize `.unwrap()` and `.expect()` — propagate errors with `?`.',
    );
  } else if (stack.language === 'Go') {
    lines.push(
      `## Stack: ${stack.name}`,
      '',
      '- Follow **Go conventions**: explicitly handle errors.',
      '- Use `context.Context` for cancellation and timeouts.',
    );
  }

  lines.push('');
  return lines;
}

function getCursorRules(stack: StackInfo): string {
  const content = [
    '# .cursorrules — Generated by ContextSlim',
    '',
    '> AI behavior rules optimized for Cursor to minimize token usage and maximize output quality.',
    '',
    ...getBaseRules(stack),
    '## Cursor Specific Instructions',
    '- Do not attempt to summarize or explain unchanged code.',
    '- Use codebase indexing effectively; do not brute-force read files.',
    '- Verify types and lint rules before providing a code block.',
  ];
  return content.join('\n');
}

function getAntigravityRules(stack: StackInfo): string {
  const content = [
    '# Antigravity General Rules',
    '',
    ...getBaseRules(stack),
    '## Antigravity Specific Instructions',
    '- Always prioritize using specific builtin tools like `grep_search` and `find_by_name` over bash commands.',
    '- Use `npx contextslim` terminal wrappers if you must use bash exploration.',
    '- When editing blocks, use `replace_file_content` instead of multi-edit unless strictly non-contiguous.',
    '- Keep line ranges tight to save tokens.',
  ];
  return content.join('\n');
}

function getClaudeRules(stack: StackInfo): string {
  const content = [
    '# Claude Code Project Context',
    '',
    ...getBaseRules(stack),
    '## Claude Specific Instructions',
    '- Provide code edits without outputting the entire file.',
    '- Avoid rambling explanations before writing code.',
    '- Stay in character as a senior developer maintaining this exact stack.',
  ];
  return content.join('\n');
}

function getCopilotInstructions(stack: StackInfo): string {
  const content = [
    '# GitHub Copilot Custom Instructions',
    '',
    ...getBaseRules(stack),
  ];
  return content.join('\n');
}

export async function generateRulesFiles(
  dir: string,
  stack: StackInfo,
): Promise<string[]> {
  const createdFiles: string[] = [];

  // 1. Cursor Rules
  const cursorPath = join(dir, '.cursorrules');
  await ensureDir(cursorPath);
  await writeFile(cursorPath, getCursorRules(stack), 'utf-8');
  createdFiles.push('.cursorrules');

  // 2. Antigravity Rules
  const antigravityPath = join(dir, '.agents', 'rules.md');
  await ensureDir(antigravityPath);
  await writeFile(antigravityPath, getAntigravityRules(stack), 'utf-8');
  createdFiles.push('.agents/rules.md');

  // 3. GitHub Copilot Instructions
  const copilotPath = join(dir, '.github', 'copilot-instructions.md');
  await ensureDir(copilotPath);
  await writeFile(copilotPath, getCopilotInstructions(stack), 'utf-8');
  createdFiles.push('.github/copilot-instructions.md');

  // 4. Claude Code / CLAUDE.md
  const claudePath = join(dir, 'CLAUDE.md');
  await ensureDir(claudePath);
  await writeFile(claudePath, getClaudeRules(stack), 'utf-8');
  createdFiles.push('CLAUDE.md');

  return createdFiles;
}
